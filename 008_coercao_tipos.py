# Coersão x Conversão
"""
Coerção
    A coerção de tipos é um conceito de linguagens de programação que força valores de diferentes tipos a interagir. 
A coerção pode ser explícita, quando o desenvolvedor escreve o código apropriado para converter entre tipos. 
A coerção pode ser implícita, quando a conversão entre tipos acontece automaticamente. 
Conversão 
    A conversão é o ato de mudar de um estado para outro, ou seja, de alterar, modificar, transformar, substituir, trocar.
    A conversão pode ser usada para induzir comportamentos e ações que direcionam a pessoa a um propósito.
"""
# type convertion, typecasting, coercion;

#Coersão
print(1+1.0)

#Conversão
print(1+1)
print(float(1+1.0))

