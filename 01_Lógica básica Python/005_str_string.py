# Python é uma linguagem de programação de tipagem dinâmica e forte, a tecnologia já sabe qual o tipo de dado fornecido;
# str ou string é um tipo de dado de textos ou sequência de caracteres;
# Strings então são textos que ficam entre aspas, Ex: "Jonathan Amancio";

#Exibindo strings com a função print:
print("Jonathan Amancio Vieira Coelho da Silva") # Usando aspas duplas
print('Dara Carolina Martins dos Santos') # Usando aspas simples
print('Jonathan \"neto\" Amancio') # Usando caracteres de escape
print(r'Jonathan \"neto\" Amancio') # Usando r no inicio do parametro ( usado em regex ), o caractere de escape é exibido