#O tipo booleano só possui dois valores possíveis, Verdadeiro ou Falso;

#para descobrir o valor usamos operadores lógicos;

## > MAIOR;
## < MENOR;
## >= MAIOR OU IGUAL;
## <= MENOR OU IGUAL;
## == IGUAL;
## != DIFERENTE;

print(True)
print(False)

print(1 == 1.0)
print(1 != 1.0)

print("Jonathan" == "Amancio")
print("Jonathan" != "Amancio")

print(10 > 11)
print(10 < 11)

print(10 >= 10)
print(10 <= 10)

#Usando a classe type() para descobrir o tipo de dado que será retornado na função print()
print(type(10 <= 10))
