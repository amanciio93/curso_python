#Tipo int -> Números do tipo inteiro positivos ou negativos;
print(1)
print(-1)
print(827194)

#Tipo float -> Números com ponto flutuante positivos ou negativos;
print(3.2)
print(-2.3)
print(-19482304.321)

# Números do tipo INT ou FLOAT sem sinal de negativo é considerado positivo;

#Usando a classe type() podemos descobrir o tipo de um dado;
print(type("Jonathan"))
print(type(-233.2))
