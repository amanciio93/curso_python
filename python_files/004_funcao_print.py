# A função print é usada para exibir algo no dispositivo de saída visual a ser usado, no nosso caso, no monitor. A função print recebe uma argumento dentro dos parenteses, que no caso é a mensagem "Olá mundo!".
print("Olá mundo!")

#É possível passar mais de um parametro:
print("Jonathan", "Dara")

# Usando o atributo sep="" podemos definir o que separa os argumentos:
print("Jonathan", "Dara", sep=" & ")

# Caractere de quebra de linha = \n.
print("Jonathan", "Dara", sep="\n")
